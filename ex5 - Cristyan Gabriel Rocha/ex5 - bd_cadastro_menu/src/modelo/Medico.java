package modelo;

/**
 *
 * @author Senai
 */
public class Medico {
    
    int id_medico;
    String nome_medico;
    String cpf_medico;
    String idade_medico;
    String tell_medico;
    String cidade_medico;
    String especialidade_medico;

    public int getId_medico() {
        return id_medico;
    }

    public void setId_medico(int id_medico) {
        this.id_medico = id_medico;
    }

    public String getNome_medico() {
        return nome_medico;
    }

    public void setNome_medico(String nome_medico) {
        this.nome_medico = nome_medico;
    }

    public String getCpf_medico() {
        return cpf_medico;
    }

    public void setCpf_medico(String cpf_medico) {
        this.cpf_medico = cpf_medico;
    }

    public String getIdade_medico() {
        return idade_medico;
    }

    public void setIdade_medico(String idade_medico) {
        this.idade_medico = idade_medico;
    }

    public String getTell_medico() {
        return tell_medico;
    }

    public void setTell_medico(String tell_medico) {
        this.tell_medico = tell_medico;
    }

    public String getCidade_medico() {
        return cidade_medico;
    }

    public void setCidade_medico(String cidade_medico) {
        this.cidade_medico = cidade_medico;
    }

    public String getEspecialidade_medico() {
        return especialidade_medico;
    }

    public void setEspecialidade_medico(String especialidade_medico) {
        this.especialidade_medico = especialidade_medico;
    }
    
    
    
}
